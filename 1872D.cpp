void solve() {
  ll n, x, y;
  cin >> n >> x >> y;

  ll l = n / x;
  ll r = n / y;
  ll d = n / lcm(x, y);

  l -= d;
  r -= d;

  ll ls = (2 * n - l + 1) * l / 2;
  ll rs = (1 + r) * r / 2;

  ll res = ls - rs;

  cout << res << endl;
}
